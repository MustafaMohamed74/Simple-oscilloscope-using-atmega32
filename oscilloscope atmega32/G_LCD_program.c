void G_LCD_WriteChar(u8 uChar)
{
	int i;
	for ( i = 0; i < 5; i++)
	{
		G_LCD_Write_Data(Font5x7[ ((uChar - 32)*5)+i]);
	}
	G_LCD_Write_Data(0x00);  // Space after each character print
}

void G_LCD_Page_Select(u8 Page ,u8 Half){
	switch (Half){
	case Left_Half :
		DIO_SetPinValue(G_CONTROL_PORT,G_CS1_PIN,PIN_LOW);
		DIO_SetPinValue(G_CONTROL_PORT,G_CS2_PIN,PIN_HIGH);
		G_LCD_Write_Command(Page);
		G_LCD_Write_Command(G_Y_StartAddress);
		break ;
	case Right_Half :
		DIO_SetPinValue(G_CONTROL_PORT,G_CS1_PIN,PIN_HIGH);
		DIO_SetPinValue(G_CONTROL_PORT,G_CS2_PIN,PIN_LOW);
		G_LCD_Write_Command(Page);
		G_LCD_Write_Command(G_Y_StartAddress);

		break ;
	}
}
