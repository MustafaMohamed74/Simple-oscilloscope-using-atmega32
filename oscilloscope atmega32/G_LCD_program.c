void G_LCD_Init(void){
	DIO_SetPortDirection(G_DATA_PORT,PORT_OUTPUT);
	DIO_SetPinDirection(G_CONTROL_PORT,G_RW_PIN,PIN_OUTPUT);
	DIO_SetPinDirection(G_CONTROL_PORT,G_RS_PIN,PIN_OUTPUT);
	DIO_SetPinDirection(G_CONTROL_PORT,G_E_PIN,PIN_OUTPUT);
	DIO_SetPinDirection(G_CONTROL_PORT,G_CS1_PIN,PIN_OUTPUT);
	DIO_SetPinDirection(G_CONTROL_PORT,G_CS2_PIN,PIN_OUTPUT);
	DIO_SetPinDirection(G_CONTROL_PORT,G_CS2_PIN,PIN_OUTPUT);

	DIO_SetPinValue(G_CONTROL_PORT,G_CS2_PIN,PIN_HIGH);

	_delay_ms(30);

	G_LCD_Write_Command(G_LCD_OFF);
	_delay_ms(3);
	G_LCD_Write_Command(G_Y_StartAddress);
	_delay_ms(3);

	G_LCD_Write_Command(G_X_Page_0);
	_delay_ms(3);

	G_LCD_Write_Command(G_Z_StartLine);
	_delay_ms(3);

	G_LCD_Write_Command(G_LCD_ON);
	_delay_ms(3);

}

void G_LCD_Write_Command(u8 Command){
	DIO_SetPortDirection(G_DATA_PORT,PORT_OUTPUT);
	DIO_SetPortDirection(G_CONTROL_PORT,PORT_OUTPUT);
	DIO_SetPinValue(G_CONTROL_PORT,G_RST_PIN,PIN_HIGH);

	DIO_SetPortValue(G_DATA_PORT,Command);
	_delay_us(3);
	DIO_SetPinValue(G_CONTROL_PORT,G_RS_PIN,PIN_LOW);
	DIO_SetPinValue(G_CONTROL_PORT,G_RW_PIN,PIN_LOW);

	DIO_SetPinValue(G_CONTROL_PORT,G_E_PIN,PIN_HIGH);
	_delay_us(3);
	DIO_SetPinValue(G_CONTROL_PORT,G_E_PIN,PIN_LOW);
	_delay_us(3);
}

void G_LCD_Write_Data(u8 Data){
	DIO_SetPortDirection(G_DATA_PORT,PORT_OUTPUT);
	DIO_SetPortDirection(G_CONTROL_PORT,PORT_OUTPUT);
	DIO_SetPinValue(G_CONTROL_PORT,G_RST_PIN,PIN_HIGH);

	DIO_SetPortValue(G_DATA_PORT,Data);
	_delay_us(3);
	DIO_SetPinValue(G_CONTROL_PORT,G_RS_PIN,PIN_HIGH);
	DIO_SetPinValue(G_CONTROL_PORT,G_RW_PIN,PIN_LOW);
	_delay_us(3);

	DIO_SetPinValue(G_CONTROL_PORT,G_E_PIN,PIN_HIGH);
	_delay_us(3);
	DIO_SetPinValue(G_CONTROL_PORT,G_E_PIN,PIN_LOW);
	_delay_us(3);
}

